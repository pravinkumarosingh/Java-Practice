package com.sugarglider.datatypes.exercises;

public class CharacterDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("Character ch contains "+ ch);

        ch++;
        System.out.println("Character ch is now "+ch);

        ch = 90;
        System.out.println("Character ch now becomes "+ch);
    }
}
